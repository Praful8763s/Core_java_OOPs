class Swapping_Array {
    public static void main(String[] args) {
        int arr[] = { 2, 4, 5, 6, 2, 1, 6, 7 };
        int largest;

        for (int i = 0; i <= 8; i++) {
            int a;
            System.out.println(arr[i]);
            largest = a;
            if (arr[i] > a) {
                largest = arr[i];
                System.out.println(largest);
            }
        }

    }
}